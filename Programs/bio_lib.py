def translate(seq):
	assert len(seq) % 3 == 0
	protein = ""
	for i in range(0, len(seq) - 2, 3):
		block = seq[i:i + 3].upper()
		if block in codons: protein += codons[block.upper()]
		else:              	protein += "X"
	return protein
